export default class CustomerData {
  constructor(id, name, email, country, orders, role, password, address, zipcode, city) {
    this.id = id;
    this.name = name;
    this.email = email;
    this.country = country;
    this.orders = orders;
    this.role = role;
    this.password = password;
    this.address = address;
    this.zipcode = zipcode;
    this.city = city;
  }
}
